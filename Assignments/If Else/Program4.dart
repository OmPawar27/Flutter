// WAP to take a number a print whether it is positive or negaitve

void main(){
    var number = 5;

    if(number>=0){
        print("$number is positive.");
    }else{
        print("$number is negative.");
    }

    number = -10;

    if(number>=0){
        print("$number is positive.");
    }else{
        print("$number is negative.");
    }

}