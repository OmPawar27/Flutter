// WAP to check if a character is a vowel or consonant

void main(){
    var character = "A";

    if(character == "A" || character == "E" || character == "I" || character == "O" || character == "U"){
        print("$character is vowel");
    }else{
        print("$character is consonant");
    }

    character = "D";

    if(character == "A" || character == "E" || character == "I" || character == "O" || character == "U"){
        print("$character is vowel");
    }else{
        print("$character is consonant");
    }
    
}