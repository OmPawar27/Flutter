// WAP in which according to month number print the number of days in that month

void main(){
    int month = 1;

    if(month%2 != 0){
        if(month <= 7){
            print("31 days");
        }else{
            print("30 days");
        }
    }

    if(month%2 != 0){
        if(month <= 7){
            print("31 days");
        }else{
            print("30 days");
        }
    }
}